Null<-function (M) 
{
  tmp <- qr(M)
  set <- if (tmp$rank == 0) 
    1:ncol(M)
  else -(1:tmp$rank)
  qr.Q(tmp, complete = TRUE)[, set, drop = FALSE]
}
